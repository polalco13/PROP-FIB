    private void changePasswordActionPerformed(java.awt.event.ActionEvent evt) {
        String currentPassword = new String(ActualPasswordField.getPassword());
        String newPassword = new String(RepeatPasswordField.getPassword());
        String repeatNewPassword = new String(NewPwPasswordField.getPassword());

        if (!newPassword.equals(repeatNewPassword)) {
            JOptionPane.showMessageDialog(this, "La nova contrasenya no coincideix.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (newPassword.equals("")) {
            JOptionPane.showMessageDialog(this, "La nova contrasenya no pot ser buida.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            if (CtrlPresentacio.comprovarContrasenya(currentPassword)) {
                CtrlPresentacio.canviarContrasenya(currentPassword, newPassword);
                JOptionPane.showMessageDialog(this, "Contrasenya canviada amb exit.");
            } else {
                JOptionPane.showMessageDialog(this, "La contrasenya actual es incorrecta.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ExcepcioUsuari e) {
            JOptionPane.showMessageDialog(this, "Error al canviar la contrasenya: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    /**
     * @brief Muestra las estadísticas del usuario logueado
     */
    public static void showStats() {
        try {
            // Obtener el nombre del usuario logueado
            String username = CtrlPresentacio.getNombreUsuarioLoggeado();
            if (username == null) {
                return;
            }

            // Obtener las estadísticas del usuario
            Integer kenkensJugats = CtrlPresentacio.getKenkensJugats();
            if (kenkensJugats != null) {
                DisplayTotalKenkensTextArea.setText(kenkensJugats.toString());
            } else {
                DisplayTotalKenkensTextArea.setText("0");
            }

            Long millorTemps = CtrlPresentacio.getMillorTempsAbsolut();
            if (millorTemps != null) {
                String millorTempsFormatted = formatTime(millorTemps);
                MillorTempsDTextArea.setText(millorTempsFormatted);
            } else {
                MillorTempsDTextArea.setText("00:00.000");
            }

            Long mitjanaTemps = CtrlPresentacio.getMitjanaTempsAbsolut();
            if (mitjanaTemps != null) {
                String mitjanaTempsFormatted = formatTime(mitjanaTemps);
                MitjaTempsTextArea.setText(mitjanaTempsFormatted);
            } else {
                MitjaTempsTextArea.setText("00:00.000");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    /**
     * @brief Formatea un tiempo en milisegundos a un string en formato mm:ss:SSS
     * @param timeInMillis Tiempo en milisegundos
     * @return Tiempo formateado en formato mm:ss:SSS
     */
    static String formatTime(long timeInMillis) {
        long minutes = (timeInMillis / 60000);
        long seconds = (timeInMillis % 60000) / 1000;
        long milliseconds = timeInMillis % 1000;

        return String.format("%02d:%02d:%03d", minutes, seconds, milliseconds);
    }



