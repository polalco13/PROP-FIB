    private void deleteProfileToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProfileToggleButtonActionPerformed
        JPasswordField pf = new JPasswordField();
        int okCxl = JOptionPane.showConfirmDialog(null, pf, "Introduir contrasenya", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (okCxl == JOptionPane.OK_OPTION) {
            String contrasenyaActual = new String(pf.getPassword());
            int confirmed = JOptionPane.showConfirmDialog(null,
                    "Estàs segur de que vols eliminar el teu perfil? Aquesta acció no es pot desfer.", "Eliminar Perfil",
                    JOptionPane.YES_NO_OPTION);

            if (confirmed == JOptionPane.YES_OPTION) {
                try {
                    if (CtrlPresentacio.comprovarContrasenya(contrasenyaActual)) {
                        CtrlPresentacio.eliminarPerfilUsuario(contrasenyaActual);
                        JOptionPane.showMessageDialog(null, "Perfil eliminat.");
                        // Cerrar la ventana actual
                        Window window = SwingUtilities.getWindowAncestor(this);
                        if (window != null) {
                            window.dispose();
                        }
                        CtrlPresentacio.ini();
                    } else {
                        JOptionPane.showMessageDialog(null, "La contrasenya es incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (ExcepcioUsuari e) {
                    JOptionPane.showMessageDialog(null, "Error en eliminar el perfil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_deleteProfileToggleButtonActionPerformed

